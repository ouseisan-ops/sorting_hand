// Split majors string by "ã€" only when not inside full-width parentheses, so names like "åˆ›ä¸šä¸åˆ›æ–°ï¼ˆä¼ä¸šå’Œè°æ²»ç†ã€åˆ›æ–°ä¸ä¼ æ‰¿æ–¹å‘ï¼‰" stay as one item.
function splitMajors(majorsStr) {
    if (Array.isArray(majorsStr)) {
        return majorsStr.map((m) => (typeof m === "string" ? m.trim() : String(m))).filter((m) => m.length > 0);
    }
    const s = typeof majorsStr === "string" ? majorsStr : "";
    const result = [];
    let current = "";
    let depth = 0;
    for (let i = 0; i < s.length; i++) {
        const c = s[i];
        if (c === "ï¼ˆ") depth += 1;
        else if (c === "ï¼‰") depth -= 1;
        else if (c === "ã€" && depth === 0) {
            if (current.trim()) result.push(current.trim());
            current = "";
            continue;
        }
        current += c;
    }
    if (current.trim()) result.push(current.trim());
    return result;
}

